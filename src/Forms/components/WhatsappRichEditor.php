    /**
     * Establece el número de filas del textarea
     */
    public function rows(int $rows): static
    {
        $this->rows = $rows;
        return $this;
    }

    /**
     * Obtiene el número de filas
     */
    public function getRows(): int
    {
        return $this->rows;
    }

    /**
     * Establece la longitud máxima del texto
     */
    public function maxLength(?int $length): static
    {
        $this->maxLength = $length;
        return $this;
    }

    /**
     * Obtiene la longitud máxima
     */
    public function getMaxLength(): ?int
    {
        return $this->maxLength;
    }

    /**
     * Establece la longitud mínima del texto
     */
    public function minLength(?int $length): static
    {
        $this->minLength = $length;
        return $this;
    }

    /**
     * Obtiene la longitud mínima
     */
    public function getMinLength(): ?int
    {
        return $this->minLength;
    }

    /**
     * Habilita el autoajuste de altura
     */
    public function autosize(bool $condition = true): static
    {
        $this->autosize = $condition;
        return $this;
    }

    /**
     * Verifica si el autoajuste está habilitado
     */
    public function isAutosize(): bool
    {
        return $this->autosize;
    }

    /**
     * Muestra u oculta las categorías de emojis
     */
    public function showEmojiCategories(bool $condition = true): static
    {
        $this->showEmojiCategories = $condition;
        return $this;
    }

    /**
     * Verifica si se muestran las categorías de emojis
     */
    public function shouldShowEmojiCategories(): bool
    {
        return $this->showEmojiCategories;
    }

    /**
     * Muestra u oculta el contador de caracteres
     */
    public function showCharacterCount(bool $condition = true): static
    {
        $this->showCharacterCount = $condition;
        return $this;
    }

    /**
     * Verifica si se muestra el contador de caracteres
     */
    public function shouldShowCharacterCount(): bool
    {
        return $this->showCharacterCount;
    }

    /**
     * Muestra u oculta la toolbar de formato
     */
    public function showToolbar(bool $condition = true): static
    {
        $this->showToolbar = $condition;
        return $this;
    }

    /**
     * Verifica si se muestra la toolbar
     */
    public function shouldShowToolbar(): bool
    {
        return $this->showToolbar;
    }

    /**
     * Establece el campo como solo lectura
     */
    public function readOnly(bool $condition = true): static
    {
        $this->readOnly = $condition;
        return $this;
    }

    /**
     * Verifica si el campo es de solo lectura
     */
    public function isReadOnly(): bool
    {
        return $this->readOnly;
    }

    /**
     * Establece categorías de emojis personalizadas
     */
    public function emojiCategories(?array $categories): static
    {
        $this->emojiCategories = $categories;
        return $this;
    }

