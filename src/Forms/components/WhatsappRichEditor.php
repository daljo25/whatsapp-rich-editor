     * Deshabilita el selector de emojis
     */
    public function disableEmojis(bool $condition = true): static
    {
        $this->disableEmojis = $condition;
        return $this;
    }

    /**
     * Verifica si los emojis están deshabilitados
     */
    public function areEmojisDisabled(): bool
    {
        return $this->disableEmojis;
    }

